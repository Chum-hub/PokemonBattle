public class Pokemons {
    public String name;
    public String firstLvlName;
    public int maxLvl;
    public int lvl;
    public int mana;
    public int hp;
    public Pokemons(String name, String firstLvlName, int maxLvl, int lvl, int mana, int hp) {
        this.name = name;
        this.firstLvlName = firstLvlName;
        this.maxLvl = maxLvl;
        this.lvl = lvl;
        this.mana = mana;
        this.hp = hp;
    }
}
